-- Crear schema si no existe
CREATE SCHEMA IF NOT EXISTS catalog;

-- Crear tabla de municipios
CREATE TABLE IF NOT EXISTS catalog.municipalities (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(100) NOT NULL,
    normalized_name VARCHAR(100) NOT NULL,
    province VARCHAR(50) NOT NULL,
    latitude DECIMAL(10, 6) NOT NULL,
    longitude DECIMAL(10, 6) NOT NULL,
    postal_codes VARCHAR(100),
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Índices para búsquedas rápidas
CREATE INDEX IF NOT EXISTS idx_municipalities_name ON catalog.municipalities(name);
CREATE INDEX IF NOT EXISTS idx_municipalities_normalized ON catalog.municipalities(normalized_name);
CREATE INDEX IF NOT EXISTS idx_municipalities_province ON catalog.municipalities(province);
CREATE INDEX IF NOT EXISTS idx_municipalities_active ON catalog.municipalities(is_active);
CREATE INDEX IF NOT EXISTS idx_municipalities_coords ON catalog.municipalities(latitude, longitude);

